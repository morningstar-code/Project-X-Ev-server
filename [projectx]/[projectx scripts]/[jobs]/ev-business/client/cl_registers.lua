local activePurchases = {}

Citizen.CreateThread(function()
    for id,register in ipairs(activeRegisters) do

        local ptId = "ev-business:register_" .. id
        exports["ev-polytarget"]:AddBoxZone(ptId, register.polytarget[1], register.polytarget[2], register.polytarget[3], register.polytarget[4])
        exports['ev-interact']:AddPeekEntryByPolyTarget(ptId, {{
            event = "ev-business:registerPurchasePrompt",
            id = ptId .. "_purchase",
            icon = "cash-register",
            label = "make payment",
            parameters = {registerId = id, business = register.business}
        }}, { distance = { radius = 2.0 } })

        exports['ev-interact']:AddPeekEntryByPolyTarget(ptId, {{
            event = "ev-business:registerChargePrompt",
            id = ptId .. "_charge",
            icon = "credit-card",
            label = "Charge Customer",
            parameters = {registerId = id, business = register.business}
        }}, { distance = { radius = 2.0 }, isEnabled = function(pEntity, pContext) return IsEmployedAt(register.business) end})
        ::continue::
    end
end)

AddEventHandler('ev-business:registerPurchasePrompt', function(pParameters, pEntity, pContext)
    local activeRegisterId = pParameters.registerId
    local activeRegister = activePurchases[activeRegisterId]
    if not activeRegister or activeRegister == nil then
        TriggerEvent("DoLongHudText", "No purchase active.")
        return
    end
    local priceWithTax = RPC.execute("PriceWithTaxString", activeRegister.cost, "Goods")
    local acceptContext = {
        {
            title = "Restaurant Purchase",
            description = "$" .. priceWithTax.text .. " | " .. activeRegister.comment,
        },
        {
            title = "Purchase with Bank",
            action = "ev-business:finishPurchasePrompt",
            icon = 'credit-card',
            key = {cost = priceWithTax.total, withoutTax = activeRegister.cost, comment = activeRegister.comment, registerId = pParameters.registerId, charger = activeRegister.charger, business = pParameters.business, cash = false},
        },
        {
            title = "Purchase with Cash",
            action = "ev-business:finishPurchasePrompt",
            icon = 'money-bill',
            key = {cost = priceWithTax.total, withoutTax = activeRegister.cost, comment = activeRegister.comment, registerId = pParameters.registerId, charger = activeRegister.charger, business = pParameters.business, cash = true},
        }
    }

    exports['ev-ui']:showContextMenu(acceptContext)
end)

AddEventHandler('ev-business:registerChargePrompt', function(pParameters, pEntity, pContext)
    exports['ev-ui']:openApplication('textbox', {
        callbackUrl = 'ev-ui:business:charge',
        inputKey = {registerId = pParameters.registerId, business = pParameters.business},
        items = {
          {
            icon = "dollar-sign",
            label = "Cost",
            name = "cost",
          },
          {
            icon = "pencil-alt",
            label = "Comment",
            name = "comment",
          },
        },
        show = true,
    })
end)

--Add to purchases at registerId pos
RegisterNetEvent('ev-business:activePurchase')
AddEventHandler("ev-business:activePurchase", function(data)
    activePurchases[data.registerId] = data
end)

--Remove at registerId pos
RegisterNetEvent('ev-business:closePurchase')
AddEventHandler("ev-business:closePurchase", function(data)
    activePurchases[data.registerId] = nil
end)

RegisterUICallback('ev-business:finishPurchasePrompt', function (data, cb)
    cb({ data = {}, meta = { ok = true, message = '' } })
    local success, message = RPC.execute("ev-business:completePurchase", data.key)
    if not success then
        TriggerEvent("DoLongHudText", message or "The purchase could not be completed.", 2)
    end
end)

RegisterUICallback("ev-ui:business:charge", function(data, cb)
    cb({ data = {}, meta = { ok = true, message = '' } })
    exports['ev-ui']:closeApplication('textbox')
    local cost = tonumber(data.values.cost)
    local comment = data.values.comment
    --check if cost is actually a number
    if cost == nil or not cost then return end
    if comment == nil then comment = "" end

    if cost < 1 then cost = 1 end --Minimum $1

    --Send event to everyone indicating a purchase is ready at specified register
    RPC.execute("ev-business:startPurchase", {cost = cost, comment = comment, registerId = data.inputKey.registerId, business = data.inputKey.business})
end)
